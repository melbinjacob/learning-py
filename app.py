color = input("Enter the first letter of a color: ")

if color.upper() == "V":
    print("Vilot")
elif color.upper() == "I":
    print("Indigo")
elif color.upper() == "B":
    print("Blue")
elif color.upper() == "G":
    print("Blue")
elif color.upper() == "Y":
    print("Yellow")
elif color.upper() == "O":
    print("Orange")
elif color.upper() == "R":
    print("Rec")
else:
    print("Invalid input")
